package voyager.quickstart.location.autonomy.bean;


public class AutnResponseData {
  public int numhits;
  public int totalhits;
  public int totaldbdocs;
  public int totaldbsecs;
  
  public int getNumhits() {
    return numhits;
  }
  public void setNumhits(int numhits) {
    this.numhits = numhits;
  }
  public int getTotalhits() {
    return totalhits;
  }
  public void setTotalhits(int totalhits) {
    this.totalhits = totalhits;
  }
  public int getTotaldbdocs() {
    return totaldbdocs;
  }
  public void setTotaldbdocs(int totaldbdocs) {
    this.totaldbdocs = totaldbdocs;
  }
  public int getTotaldbsecs() {
    return totaldbsecs;
  }
  public void setTotaldbsecs(int totaldbsecs) {
    this.totaldbsecs = totaldbsecs;
  }
}
